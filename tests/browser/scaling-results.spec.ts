import { test, expect } from '@playwright/test';
import { attachDiagnostics, saveReviewScreenshot } from './utils';

test('scaling results page renders figure, table, and csv link', async ({ page }, testInfo) => {
  const diagnostics = attachDiagnostics(page, testInfo);

  const response = await page.goto('scaling-results/', { waitUntil: 'domcontentloaded' });
  expect(response?.ok()).toBeTruthy();

  await expect(page.locator('h1')).toContainText(/Scaling results/i);

  const plot = page.locator('img[alt*="boundary scaling" i]').first();
  await expect(plot).toBeVisible();
  const naturalWidth = await plot.evaluate((img) => (img as HTMLImageElement).naturalWidth);
  expect(naturalWidth).toBeGreaterThan(0);

  const csvLink = page.getByRole('link', { name: /boundary_scaling_summary\.csv/i });
  await expect(csvLink).toBeVisible();

  const table = page.locator('main table').first();
  await expect(table).toBeVisible();
  const rowCount = await table.locator('tr').count();
  expect(rowCount).toBeGreaterThan(1);

  await expect(page.getByRole('heading', { name: /Last generated by \/ refresh model/i })).toBeVisible();

  await saveReviewScreenshot(page, testInfo, 'scaling-results');
  await diagnostics.assertAndAttach();
});
