import { test, expect } from '@playwright/test';
import { attachDiagnostics, saveReviewScreenshot } from './utils';

test('scaling results page renders figure, table, and csv link', async ({ page }, testInfo) => {
  const diagnostics = attachDiagnostics(page, testInfo);

  const response = await page.goto('scaling-results/', { waitUntil: 'domcontentloaded' });
  expect(response?.ok()).toBeTruthy();

  await expect(page.locator('h1')).toContainText(/Scaling results/i);

  const plot = page.locator('img[alt*="boundary scaling" i]').first();
  await expect(plot).toBeVisible();

  const plotSrc = await plot.getAttribute('src');
  expect(plotSrc, 'Scaling plot should define an image source').toBeTruthy();

  const browserResolvedPlotUrl = await plot.evaluate((img) => {
    const htmlImg = img as HTMLImageElement;
    return htmlImg.currentSrc || htmlImg.src;
  });

  const candidateUrls = [browserResolvedPlotUrl];
  if (plotSrc && !/^https?:/i.test(plotSrc)) {
    candidateUrls.push(new URL(plotSrc.replace(/^\/+/, ''), 'https://cu-esiil.github.io/WUI_boundary/').toString());
  }

  let plotOk = false;
  const checked: string[] = [];
  for (const candidate of candidateUrls.filter(Boolean)) {
    checked.push(candidate as string);
    const response = await page.request.get(candidate as string);
    if (response.ok()) {
      plotOk = true;
      break;
    }
  }

  expect(plotOk, `Expected scaling plot to load successfully from at least one candidate URL: ${checked.join(', ')}`).toBeTruthy();

  const csvLink = page.getByRole('link', { name: /boundary_scaling_summary\.csv/i });
  await expect(csvLink).toBeVisible();

  const table = page.locator('main table').first();
  await expect(table).toBeVisible();
  const rowCount = await table.locator('tr').count();
  expect(rowCount).toBeGreaterThan(1);

  const refreshModelHeading = page.getByRole('heading', {
    name: /Last generated by \/ refresh model/i,
  });
  const refreshSectionHeading = page.getByRole('heading', {
    name: /Refresh and reproducibility/i,
  });
  const refreshNarrative = page.getByText(
    /To refresh this figure and table|run the reproducibility workflow|scripts\/pre_pr_site_review\.sh/i,
  );
  const nextPageLink = page.locator('a[href*="fire-science-implications"]').first();

  await expect
    .poll(async () => {
      const modelCount = await refreshModelHeading.count();
      const sectionCount = await refreshSectionHeading.count();
      const narrativeCount = await refreshNarrative.count();
      const nextLinkCount = await nextPageLink.count();
      return modelCount + sectionCount + narrativeCount + nextLinkCount;
    })
    .toBeGreaterThan(0);

  await saveReviewScreenshot(page, testInfo, 'scaling-results');
  await diagnostics.assertAndAttach();
});
